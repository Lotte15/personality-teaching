export default function mapABCDEF(number) {
  switch (number) {
    case 1:
      return 'B'
    case 2:
      return 'C'
    case 3:
      return 'D'
    case 4:
      return 'E'
    case 5:
      return 'F'
    case 6:
      return 'G'
    case 7:
      return 'H'
    case 8:
      return 'I'
    case 9:
      return 'J'
    case 10:
      return 'K'
    default:
      return 'A'
  }
}
